for (let i = 0; i < 10; i++) {  // повторить 10 раз
    console.log('i = ' + i);
}
/* Инкремент
x = x + 1  <- увеличение переменной с присваиванием
x += 1
x++  <- увеличение на единицу с присваиванием
*/

for (let i = 10; i >= 0; i--) {  // повторить 10 раз
    console.log('i = ' + i);
}

/* Декремент
x = x - 1  <- увеличение переменной с присваиванием
x -= 1
x--  <- увеличение на единицу с присваиванием
*/
